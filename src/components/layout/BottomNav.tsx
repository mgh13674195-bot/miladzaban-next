export default function BottomNav() {
  const pathname = usePathname()
  const isLesson = pathname?.includes('/lessons/')
  const isAuth = pathname === '/login' || pathname === '/signup'
  if (isLesson || isAuth) return null

  return (
    <nav className="md:hidden fixed bottom-0 inset-x-0 z-50 glass border-t border-line/60 safe-area-pb">
      <div className="flex items-center justify-around h-16">
        {items.map((item) => {
          const active = pathname === item.href || (item.href !== '/' && pathname?.startsWith(item.href))
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex flex-col items-center gap-0.5 px-3 py-1.5 rounded-xl transition-all duration-200
                ${active ? 'text-primary' : 'text-ink-soft/60'}`}
            >
              {item.icon(active)}
              <span className={`text-[10px] font-bold leading-none ${active ? 'text-primary' : 'text-ink-soft/60'}`}>
                {item.label}
              </span>
            </Link>
          )
        })}
      </div>
    </nav>
  )
}
